async function main() {
  // Удалим тестовый отзыв если есть
  await prisma.review.deleteMany({ where: { source: "site", isApproved: false } });

  let created = 0;
  for (const r of reviews) {
    const sourceId = `2gis_import_${r.authorName}`;
    const exists = await prisma.review.findUnique({ where: { sourceId } });
    if (exists) continue;

    await prisma.review.create({
      data: {
        ...r,
        source: "2gis",
        sourceId,
        isApproved: true,
        isVisible: true,
      },
    });
    created++;
  }
  console.log(`Done: ${created} reviews imported`);
}

main()
  .catch(console.error)
  .finally(() => prisma.$disconnect());
